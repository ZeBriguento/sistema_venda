     */
    public function index()
    {
        if (Auth::user()->rol == 'ADMIN') {

            $data['users'] = User::where('is_deleted', '0')->count();
            $data['categories'] = Category::where('is_deleted', '0')->count();
            $data['subCategories'] = SubCategory::where('is_deleted', '0')->count();
            $data['collections'] = Collection::where('is_deleted', '0')->count();
            $data['products'] = Product::where('is_deleted', '0')->count();
            $data['clients'] = Client::all()->count();
    
            return view('admin.layouts.dashboard', compact('data'));
        } else {
            return redirect()->action([LoginController::class, 'index']);
        }
        
       
    }

